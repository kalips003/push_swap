#include "push_swap.h"

int wii_wrapper(char c);

///////////////////////////////////////////////////////////////////////////////]
/*******************************************************************************
 * 		takes in "0123456789: \nabpsr"
 * 		[0-9x] -> [0-10]
 * 		[ \nabpsr] ->[-2... -7]
 * 		else -1
*******************************************************************************/
int wii_wrapper(char c)
{
	int	i;

    if (c == 'A')
        return (0);
    if (c == 'B')
        return (1);
    i = wii(c, "0123456789: \nabpsr");// '9'  + 1 = ':'
    if (i < 11)
        return (i);
    return (9 - i);
}
